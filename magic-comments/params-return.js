/**
 * Dışarıdan 2 adet array alan ve bu arraylari birleştirip geri döndüren bir fonksiyon yazınız. 
 * Dışarıdan alınan arraylerin birleştirilmiş hali tek bir array olmalıdır.
 *
 * @param {Array} arr1 - Birinci array
 * @param {Array} arr2 - İkinci array
 * @returns {Array} Birleştirilmiş array
 */
function mergeArrays(arr1, arr2) {
    return arr1.concat(arr2);
}
